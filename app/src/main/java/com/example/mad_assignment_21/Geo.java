package com.example.mad_assignment_21;

public class Geo {

    private String lat;
    private String longi;

    public Geo(String lat, String longi) {
        this.lat = lat;
        this.longi = longi;
    }

    public String getLat()
    {
        return lat;
    }

    public String getLongi() {
        return longi;
    }
}


